 using System;

 class DeclaracaoVariaveis
{
	static void Main()
	{
			/*Declaração de variavel
			*/
		int a ; 
		//inicializacao da variavel
		a = 22;
		Console.WriteLine(a);
		
		//Reatribuição de valor da variavel
		
		a=56;
		Console.WriteLine(a);
		
		//Outras formas de declarar variaveis
		
		//Declaracao e inicializacao a variavel
		int b = 925;
		Console.WriteLine(b);
		int y = 10, x = 20;
		int z = y + x;
	}
}